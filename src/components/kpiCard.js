export function renderKPICard({ label, value, sub, color = 'indigo', icon = 'money', isCurrency = true, delta, deltaLabel }) {
  const displayValue = isCurrency
    ? formatarMoeda(value)
    : (typeof value === 'number' ? value.toLocaleString('pt-BR') : value);
  const deltaHtml = delta !== undefined ? `
    <div class="kpi-delta ${delta >= 0 ? 'kpi-delta-up' : 'kpi-delta-down'}">
      <svg width="12" height="12" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2.5"><polyline points="${delta >= 0 ? '18 15 12 9 6 15' : '6 9 12 15 18 9'}"/></svg>
      ${Math.abs(delta).toFixed(1)}%${deltaLabel ? ` <span class="kpi-delta-label">${deltaLabel}</span>` : ''}
    </div>
  ` : '';

  return `
    <div class="kpi-card ${color}">
      <div class="kpi-card-top">
        <div class="kpi-icon ${color}">
          ${KPI_ICONS[icon] || KPI_ICONS.money}
        </div>
        ${deltaHtml}
      </div>
      <div class="kpi-label">${label}</div>
      <div class="kpi-value" data-animate>${displayValue}</div>
      ${sub ? `<div class="kpi-sub">${sub}</div>` : ''}
    </div>
  `;
}

export function renderKPIGrid(cards) {
  return `<div class="kpi-grid">${cards.map(c => renderKPICard(c)).join('')}</div>`;
}
